const dictionaries: Record<Language, Record<string, string>> = { zh, en };

interface I18nContextValue {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: string) => string;
  translateDynamic: (prefix: 'event.type' | 'event.message', value: string) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return stored === 'en' || stored === 'zh' ? stored : 'zh';
  });

  const dictionary = dictionaries[language];

  const value = useMemo<I18nContextValue>(() => {
    const setLanguage = (next: Language) => {
      window.localStorage.setItem(LANGUAGE_STORAGE_KEY, next);
      setLanguageState(next);
    };
    const t = (key: string) => dictionary[key] ?? key;
    const translateDynamic = (prefix: 'event.type' | 'event.message', raw: string) => {
      const key = `${prefix}.${raw}`;
      return dictionary[key] ?? raw;
    };
    return { language, setLanguage, t, translateDynamic };
  }, [dictionary, language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) {
    throw new Error('useI18n must be used inside I18nProvider');
  }
  return value;
}
